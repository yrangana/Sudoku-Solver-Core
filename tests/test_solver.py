def test_is_valid_grid():
    valid_grid = [
        [5, 3, 0, 0, 7, 0, 0, 0, 0],
        [6, 0, 0, 1, 9, 5, 0, 0, 0],
        [0, 9, 8, 0, 0, 0, 0, 6, 0],
        [8, 0, 0, 0, 6, 0, 0, 0, 3],
        [4, 0, 0, 8, 0, 3, 0, 0, 1],
        [7, 0, 0, 0, 2, 0, 0, 0, 6],
        [0, 6, 0, 0, 0, 0, 2, 8, 0],
        [0, 0, 0, 4, 1, 9, 0, 0, 5],
        [0, 0, 0, 0, 8, 0, 0, 7, 9],
    ]
    invalid_grid = [
        [5, 3, 0, 0, 7],  # Not 9x9
        [6, 0, 0, 1, 9, 5],
        [0, 9, 8, 0],
    ]
    assert is_valid_grid(valid_grid)  # Should return True
    assert not is_valid_grid(invalid_grid)  # Should return False
