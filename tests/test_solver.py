def test_invalid_grid():
    invalid_puzzle = [
        [5, 3, 0, 0, 7],  # Invalid row length
        [6, 0, 0, 1, 9, 5, 0, 0, 0],
    ]
    assert not is_valid_grid(invalid_puzzle)
    with pytest.raises(ValueError):
        solve_sudoku_with_timeout(invalid_puzzle)


def test_unsolvable_puzzle():
    unsolvable_puzzle = [
        [5, 3, 0, 0, 7, 0, 0, 0, 0],
        [6, 0, 0, 1, 9, 5, 0, 0, 0],
        [0, 9, 8, 0, 0, 0, 0, 6, 0],
        [8, 0, 0, 0, 6, 0, 0, 0, 3],
        [4, 0, 0, 8, 0, 3, 0, 0, 1],
        [7, 0, 0, 0, 2, 0, 0, 0, 6],
        [0, 6, 0, 0, 0, 0, 2, 8, 0],
        [0, 0, 0, 4, 1, 9, 0, 0, 5],
        [5, 0, 0, 0, 8, 0, 0, 7, 9],  # Two '5's in the first column
    ]
    assert is_valid_grid(unsolvable_puzzle)
    with pytest.raises(Exception):
        solve_sudoku_with_timeout(unsolvable_puzzle, timeout=10)


